import BottomBar from './components/BottomBar.vue'
import Toast from 'components/common/toast/index.vue'
import { getDetail } from 'network/detail'
import {BackTopMixin} from 'common/mixn'
import {  } from 'store/mutation.type'
import { mapActions } from 'vuex'


export default {
  name: 'detailVue',
  data () {
    return {
      iid: this.$route.query.id,
      baseInfo: {},
      shopInfo: {},
      themeTopY: [0, -1000, -2000, -3000],
      currentIndex: 0,
      message: '',
      isShow: false
    }
  },
  mixins: [BackTopMixin],
  components: {
    NavBar,
    BaseInfo,
    ShopInfo,
    Scroll,
    BottomBar,
    Toast
  },
  computed: {
  },
  created () {
    this.getDetail()
  },
  mounted() {
    // this.themeTopY = [0, this.$refs.base.$el.offsetTop, this.$refs.shop.$el.offsetTop, this.$refs.ul.offsetTop]
    // console.log('this.themeTopY', this.themeTopY)
    
  },
  updated () {
    // this.$nextTick(() => {
    //   this.themeTopY = [0, this.$refs.base.$el.offsetTop, this.$refs.shop.$el.offsetTop, this.$refs.ul.offsetTop]
    //   console.log('this.themeTopY', this.themeTopY)
    // })
  },
  methods: {
    ...mapActions({
      addCart: 'addToCart'
    }),
    initBaseInfo (result) {
      const {
        columns,
        itemInfo: {oldPrice, price: newPrice, desc, discountDesc, topImages, lowPrice},
        shopInfo: {
          services = []
        }
      } = result
      return {
        columns,
        services,
        oldPrice,
        newPrice,
        desc,
        discountDesc,
        topImages,
        lowPrice
      }
    },
    initShopInfo (result) {
      return result.shopInfo
    },
    getDetail () {
      getDetail(this.iid).then(res => {
        const {result = {}} = res
        this.baseInfo = this.initBaseInfo(result)
        this.shopInfo = this.initShopInfo(result)
        // this.$refs.scroll.refresh()
        this.$nextTick(() => {
          this.themeTopY = [0, this.$refs.base.$el.offsetTop, this.$refs.shop.$el.offsetTop, this.$refs.ul.offsetTop, Number.MAX_VALUE]
        })
      })
    },
    titleClick (index) {
      this.$refs.scroll.scrollTo(0, -this.themeTopY[index], 100)
      if (index > 1) {
        this.showBackTop = true
      }
    },
    contentScroll (position) {
      const y = -position.y
      for (let i = 0 ; i < this.themeTopY.length; i++) {
        if (this.currentIndex !== i && (y >= this.themeTopY[i] && y < this.themeTopY[i+1])) {
          this.currentIndex = i
          this.$refs.nav.currentIndex = this.currentIndex
        }
      }
      if (y > 200) {
        this.showBackTop = true
      }
    },
    backClick1 () {
      this.$refs.bottom.backClick()
    },
    addToCart () {
      // 获取购物车需要展示的商品信息
      const product = {
        image: this.baseInfo.topImages[0],
        title: this.baseInfo.title,
        desc: this.baseInfo.desc,
        iid: this.iid,
        realPrice: this.baseInfo.lowPrice
      }
      // this.$store.dispatch('addToCart',product).then(res => {
      //   console.log(res)
      // })
      this.addCart(product).then(res => {
        // 提示添加购物车this.$toast.show(message, 1000)
        // this.message = res
        // this.isShow = true
        // setTimeout(() => {
        //     this.isShow = false
        //   }, 1000)
        this.$toast.show(res)
        })
    },
    
  }
}
